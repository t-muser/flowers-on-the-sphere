# Public entry point
# ─────────────────────────────────────────────────────────────────────────────

def run_simulation(
    params: dict[str, Any],
    out_dir: Path,
    config: RunConfig | None = None,
    **overrides: Any,
) -> None:
    """Run one MITgcm Held-Suarez simulation and write a benchmark Zarr.

    Implements the two-phase strategy: spin-up (no output) followed by data
    collection (daily snapshots). Both phases share the same ``run_dir``; the
    spin-up leaves a pickup file that the data phase restarts from.

    Args:
        params:   Physical parameter dict from ``generate_sweep.py``.  Keys:
                  ``tau_drag_days``, ``tau_surf_days``, ``delta_T_y``,
                  ``delta_theta_z``, ``seed``.  ``tau_atm_days`` defaults to 40.
        out_dir:  Root output directory.  The MITgcm working directory is
                  created as ``out_dir/mitgcm_run/``; the final Zarr is at
                  ``out_dir/run.zarr``.
        config:   Numerical / infrastructure configuration.  Defaults to
                  ``RunConfig()`` if ``None``.
        **overrides: Per-call overrides for individual ``RunConfig`` fields.

    Raises:
        RuntimeError: If the MITgcm executable returns a non-zero exit code.
        FileNotFoundError: If the compiled executable does not exist.
        ImportError: If ``xmitgcm`` is not installed.
    """
    base = config if config is not None else RunConfig()
    cfg = replace(base, **overrides) if overrides else base

    out_dir = Path(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    run_dir = out_dir / "mitgcm_run"

    executable = Path(cfg.executable)
    if not executable.exists():
        raise FileNotFoundError(
            f"MITgcm executable not found: {executable}. "
            "Run datagen/mitgcm/scripts/build.py first."
        )

    input_dir = Path(cfg.input_dir)
    sim_params = SimulationParams.from_dict(params)

    logger.info(
        "Starting MITgcm run: τ_drag=%.1f d  τ_surf=%.1f d  "
        "ΔTy=%.1f K  Δθz=%.1f K  seed=%d",
        1.0 / (sim_params.kf * 86400.0),
        1.0 / (sim_params.ks * 86400.0),
        sim_params.delta_T_y,
        sim_params.delta_theta_z,
        sim_params.seed,
    )

    # ── Phase 1: spin-up ─────────────────────────────────────────────────────
    logger.info("Phase 1: spin-up (%.0f days, no output)", cfg.spinup_days)
    _setup_run_dir(run_dir, cfg, sim_params, phase=1,
                   executable=executable, input_dir=input_dir)
    _launch_mitgcm(run_dir, cfg, phase=1)

    # ── Phase 2: data collection ──────────────────────────────────────────────
    logger.info("Phase 2: data collection (%.0f days, daily snapshots)", cfg.data_days)
    _setup_run_dir(run_dir, cfg, sim_params, phase=2,
                   executable=executable, input_dir=input_dir)
    _launch_mitgcm(run_dir, cfg, phase=2)

    # ── Read output and write Zarr ────────────────────────────────────────────
    logger.info("Reading MDS output and writing Zarr")
    ds = _read_mitgcm_output(run_dir, cfg)
    _write_zarr(ds, out_dir / "run.zarr", cfg, params)
