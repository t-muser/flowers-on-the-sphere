@dataclass(frozen=True)
class RunConfig:
    """Numerical and infrastructure settings for one MITgcm run.

    Physical parameter choices (forcing timescales, temperature differences)
    live in ``SimulationParams`` and vary across the ensemble. Settings here
    are fixed for a given compiled binary and hardware configuration.

    Attributes:
        Nlon:          Number of longitude grid points (must match SIZE.h).
        Nlat:          Number of latitude grid points (must match SIZE.h).
        Nr:            Number of vertical pressure levels (must match SIZE.h).
        n_mpi:         Number of MPI ranks to launch (must be consistent with
                       nPy × nPx × nSx × nSy in SIZE.h; default 4 = nPy=4).
        delta_t:       Timestep [s]. Must satisfy CFL for the given resolution.
        spinup_days:   Spin-up duration [days] — run without output.
        data_days:     Data-collection duration [days] — daily snapshots saved.
        snapshot_interval_days: Output cadence [days].
        pressure_hpa:  Pressure level [hPa] to extract for u, v, T output.
        executable:    Path to the compiled ``mitgcmuv`` binary.
        input_dir:     Directory of static input files (bathymetry, grid)
                       symlinked into every run dir. Defaults to ``input/``
                       beside the executable's package, generated by build.py.
        mpirun_cmd:    MPI launcher command (list of strings prepended to the
                       executable invocation; set to ``[]`` for serial runs).
        timeout_factor: Wall-clock timeout = ``total_sim_seconds × factor``.
    """
    # Grid — must match SIZE.h compile-time constants.
    Nlon: int = 128
    Nlat: int = 64
    Nr: int = 20
    n_mpi: int = 4

    # Time.
    delta_t: float = 600.0
    spinup_days: float = 200.0
    data_days: float = 365.0
    snapshot_interval_days: float = 1.0

    # Output.
    pressure_hpa: float = 500.0

    # Infrastructure.
    executable: Path = field(default_factory=lambda: _DEFAULT_EXECUTABLE)
    input_dir: Path = field(default_factory=lambda: _DEFAULT_INPUT_DIR)
    mpirun_cmd: tuple[str, ...] = ("mpirun", "-n", "{n_mpi}")
    timeout_factor: float = 3.0

    # ── Derived quantities ────────────────────────────────────────────────────
    @property
    def spinup_seconds(self) -> float:
        return self.spinup_days * 86400.0

    @property
    def data_seconds(self) -> float:
        return self.data_days * 86400.0

    @property
    def snapshot_interval_s(self) -> float:
        return self.snapshot_interval_days * 86400.0

    @property
    def spinup_steps(self) -> int:
        return round(self.spinup_seconds / self.delta_t)

    @property
    def data_steps(self) -> int:
        return round(self.data_seconds / self.delta_t)

    @property
    def timeout_s(self) -> float:
        return (self.spinup_seconds + self.data_seconds) * self.timeout_factor


@dataclass(frozen=True)
class SimulationParams:
    """Physical parameters of one Held-Suarez ensemble member.

    All forcing timescales are stored internally as rates [1/s] to match the
    Fortran namelist convention.  The ``from_dict`` constructor accepts the
    natural user-facing representation (timescales in days).
    """
    kf: float            # Rayleigh surface-drag rate [1/s]
    ka: float            # Free-atmosphere Newtonian cooling rate [1/s]
    ks: float            # Surface Newtonian cooling rate [1/s]
    delta_T_y: float     # Equator-to-pole temperature difference [K]
    delta_theta_z: float # Surface-to-tropopause temperature difference [K]
    seed: int            # RNG seed for IC perturbation

    @classmethod
    def from_dict(cls, d: dict[str, Any]) -> "SimulationParams":
        """Build from a JSON param dict (timescales in days, ΔT in K)."""
        return cls(
            kf=1.0 / (float(d["tau_drag_days"]) * 86400.0),
            ka=1.0 / (float(d.get("tau_atm_days", 40.0)) * 86400.0),
            ks=1.0 / (float(d["tau_surf_days"]) * 86400.0),
            delta_T_y=float(d["delta_T_y"]),
            delta_theta_z=float(d["delta_theta_z"]),
            seed=int(d["seed"]),
        )


