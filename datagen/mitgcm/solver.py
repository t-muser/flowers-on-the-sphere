"""MITgcm Held-Suarez GCM solver: orchestrates compilation output → Zarr.

The ``run_simulation`` entry point follows the same interface as all other
datagen solvers (``params: dict``, ``out_dir: Path``, ``config``,
``**overrides``), but MITgcm is a compiled Fortran/C binary rather than a
Python library. The internal implementation therefore:

1. Sets up a working directory with namelists, IC files, and symlinks.
2. Runs MITgcm via ``subprocess`` in **two sequential phases**:
   - *Spin-up* (``spinup_days``, no diagnostic output) — lets the atmosphere
     develop realistic jets and eddies from the initial state.
   - *Data collection* (``data_days``, daily snapshots) — restarts from the
     spin-up pickup file and writes the output Zarr.
3. Reads the tiled MDS binary output and extracts fields at the configured
   pressure level plus surface pressure.
4. Writes a ``(time, field, lat, lon)`` float32 Zarr via
   ``datagen.resample.write_latlon_zarr``.

The two-phase design avoids wasting disk space on spin-up snapshots: the
data-collection phase only starts diagnostic output after the atmosphere
has reached statistical equilibrium.

Requires:
  - A compiled ``mitgcmuv`` executable (produced by ``scripts/build.py``).
"""

from __future__ import annotations

import logging
import os
import re
import subprocess
import time
from dataclasses import dataclass, field, replace
from pathlib import Path
from typing import Any

import numpy as np

from datagen.mitgcm._constants import (
    HS_KA,
    HS_KF,
    HS_KS,
    HS_DELTA_T_Y,
    HS_DELTA_T_Z,
    HS_SIGMAB,
    HS_T0,
    P0,
)
from datagen.mitgcm.ic import write_bathymetry, write_temperature_ic
from datagen.mitgcm.namelist import write_all_namelists
from datagen.resample import write_latlon_zarr, regular_lat_grid, regular_lon_grid

logger = logging.getLogger(__name__)

# Default executable path — overridden by RunConfig.executable.
_DEFAULT_EXECUTABLE = Path(__file__).parent / "build" / "mitgcmuv"
# Default static-input directory (bathymetry, grid files) symlinked into each
# run dir. Sits next to build/, generated by datagen/mitgcm/scripts/build.py.
_DEFAULT_INPUT_DIR = Path(__file__).parent / "input"


